
public class Literal {

	public static void main(String[] args) {
		// Exemplos de Literal
		
		int numero;
		String cadeiaCaractere;
		char caracter;
		boolean certoOuErrado;
		
		numero = 20;
		cadeiaCaractere = "**Meu Curso de Java Fundamentos";
		caracter = 'N';
		certoOuErrado = false;
		
		System.out.println("O valor de número é : " + numero);
		System.out.println("O valor de cadeiCaracter é : " + cadeiaCaractere);
		System.out.println("O valor de caracter é : " + caracter);
		System.out.println("O valor de certoOuErrado é : " + certoOuErrado);

	}

}
